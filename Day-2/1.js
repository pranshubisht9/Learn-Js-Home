/* What is variable in coding?

A variable is a named unit of data that is assigned a value.

Think of a variable as a container or a box that can hold something. It has a name and can store different kinds of things like numbers or words.

For example, let's say you have a box named "toys." You can put different toys inside the box like cars, action figures, or stuffed animals. The box is like a variable, and the toys inside are like the values that the variable can store. */

let age = 10;
//variable name is age and value is 10

let weight = 50.24;
//variable name is weight and value is 50.24

let name = "Gaurav"; 
//variable name is name and value is Gaurav

let character = 'a';
//variable name is character and value is a


//Rules for naming variables
//1. Variable names must begin with a letter, an underscore (_) or a dollar sign ($).
let $age = 12

//2. Variable names can only contain letters, numbers, underscores, or dollar signs.
let age2 = 12

//3. Variable names are case-sensitive. For example, score and Score are two different variables.

let score = 25 
let SCORE = 20

console.log(score); //25
console.log(SCORE); //20

//4. Reserved keywords (like let, var, or const) cannot be used as variable names.

//5. Variable names should describe the data they hold. (name should be meaningful)



